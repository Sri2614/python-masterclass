# String Slicing - Create a substring by extracting elements from another string indexing[] or slice() [start:stop:step]

name = "Sri Balaji"

first_name = name[::3]
reve_name = name[::]
 
print(first_name)
print(reve_name)

website = "http://google.com"

print(len(website))
slice = slice(7,-4)

print(website[slice])
