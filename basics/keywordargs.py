# Keyword arguments - Thet are preceded by an identifier when we pass them to a function.The order of the arguments doesn't matter, unlike positional arguments.

def argsfunc(first_name, middle_name, last_name):
    print("Hey "+first_name + " "+ middle_name+ " "+ last_name)

argsfunc(last_name="Balaji",middle_name="B.B",first_name="Sri")